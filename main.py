# Sleep entry endpoints
class SleepEntryCreate(BaseModel):
    date: str
    bedtime: str
    waketime: str
    quality: int = 3
    notes: Optional[str] = None

@app.post("/api/sleep", status_code=201)
async def create_sleep_entry(entry: SleepEntryCreate):
    try:
        # Validate via schema
        validated = Sleepentry(**entry.model_dump())
        inserted_id = create_document("sleepentry", validated)
        return {"id": inserted_id}
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

@app.get("/api/sleep")
async def list_sleep_entries(limit: int = 50):
    try:
        docs = get_documents("sleepentry", {}, limit)
        # Convert ObjectId and datetime to strings
        def serialize(doc):
            doc = {**doc}
            if "_id" in doc:
                doc["id"] = str(doc.pop("_id"))
            for k, v in list(doc.items()):
                if hasattr(v, "isoformat"):
                    doc[k] = v.isoformat()
            return doc
        return [serialize(d) for d in docs]
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

